package choco.cp.solver.search.task.ordering;

import choco.cp.solver.constraints.global.scheduling.precedence.ITemporalSRelation;

public final class MaxPreservedOrdering extends RandomOrdering {

	 public MaxPreservedOrdering(long seed) {
		super(seed);
	}

	 @Override
	public int getBestVal(ITemporalSRelation rel) {
		return getMaxVal(rel.getBackwardPreserved(), rel.getForwardPreserved());
	}
}
