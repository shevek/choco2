public final class MinEnv implements ValSelector<SetVar> {

	
  public int getBestVal(SetVar v) {
    int val = Integer.MIN_VALUE;
    DisposableIntIterator it = v.getDomain().getEnveloppeIterator();
    while (it.hasNext()) {
      val = it.next();
      if (!v.isInDomainKernel(val)) {
          break;
      }
    }
    it.dispose();
    return val;
  }


}
