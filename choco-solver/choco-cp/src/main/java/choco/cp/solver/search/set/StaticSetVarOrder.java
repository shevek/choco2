public class StaticSetVarOrder extends AbstractSetVarSelector {

  public StaticSetVarOrder(SetVar[] vars) {
    this.vars = vars;
  }

  public SetVar selectSetVar() {
    for (int i = 0; i < vars.length; i++) {
      if (!vars[i].isInstantiated()) {
        return vars[i];
      }
    }
    return null;
  }

}
