package i_want_to_use_this_old_version_of_choco.palm.dbt.search;

import i_want_to_use_this_old_version_of_choco.Constraint;
import i_want_to_use_this_old_version_of_choco.palm.PalmConstraint;
import i_want_to_use_this_old_version_of_choco.palm.search.SymbolicDecision;

/**
 * An enumeration constraint like instantiation.
 */

public interface DecisionConstraint extends PalmConstraint, SymbolicDecision {

  /**
   * Returns the negation of this enumeration constraint.
   */

  public Constraint negate();


  /**
   * Returns the number identifying the current branch.
   */

  public int getBranch();
}
