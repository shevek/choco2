public abstract class DisposableIterator implements Iterator {

    public boolean reusable;

    public void init() {
        reusable = false;
    }

    /**
     * This method allows to declare that the iterator is not usefull anymoure. It
     * can be reused by another object.
     */
    public void dispose() {
        reusable = true;
    }

}
