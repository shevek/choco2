public class ASAPSkill {




    String id;
    String label;



    public ASAPSkill(ASAPItemHandler handler, String id)
    {
        this.id = id;
        handler.putSkill(id,this);
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}